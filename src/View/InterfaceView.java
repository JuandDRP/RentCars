    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        int id=Integer.parseInt(jTextField1.getText());
        String tipo=jComboBox2.getSelectedItem().toString();
        String metodoPago=jComboBox1.getSelectedItem().toString();
        String nuevoEstado=jTextField2.getText();
        LocalDate fechaInicio=LocalDate.parse(jTextField7.getText());
        LocalDate fechaFin=LocalDate.parse(jTextField8.getText());
        long dias=ChronoUnit.DAYS.between(fechaInicio,fechaFin);
        //Si el tipo es carro, invocar al metodo de reservar carro
        if(tipo.equalsIgnoreCase("Car")||tipo.equalsIgnoreCase("Carro")){
          //logica para alter table car 
            ViewController.reservarCar(id, nuevoEstado);
            
            ViewController.hidden();
            TicketController.show();
            TicketController.generarTicketCar(id, dias,fechaInicio,fechaFin,tipo,metodoPago);
            
                   
          
        }else{       
          //logica para alter table moto
            ViewController.reservarMotorcycle(id, nuevoEstado);
            ViewController.hidden();
            TicketController.show();
            TicketController.generarTicketMoto(id, dias,fechaInicio,fechaFin,tipo,metodoPago);
            
          
        }
        
// TODO add your handling code here:
    }//GEN-LAST:event_jToggleButton1ActionPerformed

